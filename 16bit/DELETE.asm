    PUSH DI
    CLD
    XOR BX, BX
    MOV AH, 1
    INT 21H

; Vong lap
WHILE_:
    CMP AL, 13
    JE END_WHILE
    CMP AL, 8
    JNE ELSE1
    DEC DI
    DEC BX
    JMP READ
